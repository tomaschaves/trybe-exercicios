function reduceNames() {
  return books.reduce((acc, array) => `${acc} ${array.author.name}.`, '');
}
console.log(`Os nomes das pessoas autoras são:${reduceNames()}`);

// Calcule a média de idade que as pessoas autoras tinham quando seus respectivos livros foram lançados. expectedResult = 43;

function averageAge() {
  const numberOfAuthors = books.length;
  const ages = books.reduce((sum, book) => (
    sum + (book.releaseYear - book.author.birthYear)
  ), 0);
  return ages / numberOfAuthors;
}

console.log(`\nA média de idade das pessoas autoras é: ${averageAge()}`);

